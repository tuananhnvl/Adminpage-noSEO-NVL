<p>
    <strong>Điều 25. Giải quyết tranh chấp nội bộ</strong>
    <br/>
    Trường hợp phát sinh tranh chấp hay khiếu nại có liên quan tới hoạt động
    của Công ty hay tới quyền của các cổ đông phát sinh từ Điều lệ hay từ bất
    cứ quyền hoặc nghĩa vụ do Luật Doanh nghiệp hay các luật khác hoặc các quy
    định hành chính quy định, giữa:
    <br/>
    + Giám đốc với Công ty;
</p>
<div>
    <p>
        + Nhân viên với công ty;
    </p>
    <p>
        - Các bên liên quan sẽ cố gắng giải quyết tranh chấp đó thông qua
        thương lượng và hoà giải.Trường hợp không đạt được quyết định hoà giải
        trong vòng sáu tuần từ khi bắt đầu quá trình hòa giải hoặc nếu quyết
        định của trung gian hoà giải không được các bên chấp nhận, bất cứ bên
        nào cũng có thể đưa tranh chấp đó ra Trung tâm trọng tài quốc tế Việt
        Nam- bên cạnh phòng thương mại công nghiệp Việt Nam tại Thành phố Hồ
        Chí Minh.
        <br/>
        a) Ngôn ngữ được sử dụng trong tố tụng trọng tài sẽ là tiếng Việt, trừ
        khi vụ việc tranh chấp có yếu tố nước ngoài.
        <br/>
        b) Các quy tắc trọng tài áp dụng cho việc giải quyết theo thủ tục rút
        gọn của Trung tâm trọng tài quốc tế Việt Nam sẽ được áp dụng, trừ khi
        vụ việc có tính chất phức tạp, số tiền tranh chấp lớn và các trường hợp
        khác trung tâm trọng tài có thể quyết định áp dụng Quy tắc Trọng tài
        thông thường của họ. Trong trường hợp đó, trung tâm trọng tài được lựa
        chọn cũng sẽ quyết định liệu hội đồng trọng tài sẽ bao gồm một hoặc ba
        trọng tài viên.
        <br/>
        c) Các cổ đông cam kết và đồng ý rằng tất cả các thủ tục tố tụng trọng
        tài được tiến hành có liên quan đến Điều khoản trọng tài này sẽ được
        giữ bí mật. Mặc dù vậy, một cổ đông sẽ không bị ngăn cản tiết lộ thông
        tin đó để bảo vệ theo cách tốt nhất có thể các quyền của mình đối với
        các cổ đông khác liên quan đến tranh chấp, hoặc nếu quyền đó được quy
        định tại các điều khoản, quy định, một quyết định của một cơ quan có
        thẩm quyền, một quy tắc trao đổi chứng khoán hoặc tương tự.
        <br/>
        d) Bất kỳ cổ đông nào, trước hoặc trong khi tiến hành thủ tục tố tụng
        trọng tài đều có thể yêu cầu đến tòa án có thẩm quyền để thực hiện biện
        pháp khẩn cấp tạm thời khi xét thấy như vậy là biện pháp cần thiết để
        bảo vệ quyền lợi của mình trong khi chờ thực hiện xong thủ tục tố tụng
        trọng tài.
    </p>
    <p>
        - Các bên sẽ tự chịu chi phí của mình có liên quan tới thủ tục thương
        lượng và hoà giải. Các chi phí của Trọng tài do bên thua kiện chịu.
    </p>
</div>
