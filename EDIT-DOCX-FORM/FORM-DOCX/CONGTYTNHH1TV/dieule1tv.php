<p>
    <strong>Điều 13. Cơ cấu tổ chức quản lý</strong>
</p>
<p>
    Chủ tịch công ty:<strong> NGUYỄN THỊ MINH NGA</strong>
</p>
<p>
    Giám đốc:<strong> NGUYỄN THỊ MINH NGA</strong>
</p>
<p>
    <strong>Điều 14.Chủ tịch công ty</strong>
</p>
<p>
    1. Chủ tịch công ty có quyền nhân danh công ty thực hiện các quyền và nghĩa
    vụ của công ty; chịu trách nhiệm trước pháp luật về việc thực hiện các
    quyền và nhiệm vụ được giao theo quy định của Luật Doanh nghiệp và pháp
    luật có liên quan
</p>
<p>
    2. Quyền, nghĩa vụ, nhiệm vụ cụ thể và chế độ làm việc của Chủ tịch công ty
    được thực hiện theo quy định tại Điều lệ công ty và pháp luật có liên quan.
</p>
<p>
    <strong>Điều 15. Giám đốc hoặc Tổng giám đốc</strong>
</p>
<p>
    1. Nhiệm kỳ Giám đốc hoặc Tổng giám đốc không quá năm năm. Giám đốc hoặc
    Tổng giám đốc chịu trách nhiệm trước pháp luật và Chủ tịch công ty về việc
    thực hiện các quyền và nhiệm vụ của mình.
</p>
<p>
    2. Giám đốc hoặc Tổng giám đốc có các quyền sau đây:
</p>
<p>
    a) Tổ chức thực hiện quyết định của Chủ tịch công ty;
</p>
<p>
    b) Quyết định các vấn đề liên quan đến hoạt động kinh doanh hằng ngày của
    công ty;
</p>
<p>
    c) Tổ chức thực hiện kế hoạch kinh doanh và phương án đầu tư của công ty;
</p>
<p>
    d) Ban hành quy chế quản lý nội bộ công ty;
</p>
<p>
    đ) Bổ nhiệm, miễn nhiệm, cách chức các chức danh quản lý trong công ty, trừ
    các chức danh thuộc thẩm quyền của Chủ tịch công ty;
</p>
<p>
    e) Ký kết hợp đồng nhân danh công ty, trừ trường hợp thuộc thẩm quyền của
    Chủ tịch Công ty;
</p>
<p>
    g) Kiến nghị phương án cơ cấu tổ chức công ty;
</p>
<p>
    h) Trình báo cáo quyết toán tài chính hằng năm lên Chủ tịch Công ty;
</p>
<p>
    i) Kiến nghị phương án sử dụng lợi nhuận hoặc xử lý lỗ trong kinh doanh;
</p>
<p>
    k) Tuyển dụng lao động;
</p>
<p>
    l) Các quyền khác được quy định tại Điều lệ công ty, hợp đồng lao động mà
    Giám đốc hoặc Tổng giám đốc ký với Chủ tịch công ty.
</p>
<p>
    <strong>Điều 16. Kiểm soát viên</strong>
</p>
<p>
    1. Chủ sở hữu công ty bổ nhiệm một đến ba Kiểm soát viên với nhiệm kỳ không
    quá ba năm. Kiểm soát viên chịu trách nhiệm trước pháp luật và chủ sở hữu
    công ty về việc thực hiện các quyền và nhiệm vụ của mình.
</p>
<p>
    2. Kiểm soát viên có các nhiệm vụ sau đây:
</p>
<p>
    a) Kiểm tra tính hợp pháp, trung thực, cẩn trọng của Giám đốc hoặc Tổng
    giám đốc trong tổ chức thực hiện quyền chủ sở hữu, trong quản lý điều hành
    công việc kinh doanh của công ty;
</p>
<p>
    b) Thẩm định báo cáo tài chính, báo cáo tình hình kinh doanh, báo cáo đánh
    giá công tác quản lý và các báo cáo khác trước khi trình chủ sở hữu công ty
    hoặc cơ quan nhà nước có liên quan; trình chủ sở hữu công ty báo cáo thẩm
    định;
</p>
<p>
    c) Kiến nghị chủ sở hữu công ty các giải pháp sửa đổi, bổ sung, cơ cấu tổ
    chức quản lý, điều hành công việc kinh doanh của công ty;
</p>
<p>
    d) Các nhiệm vụ khác theo yêu cầu, quyết định của chủ sở hữu công ty.
</p>
<p>
    3. Kiểm soát viên có quyền xem xét bất kỳ hồ sơ, tài liệu nào của công ty
    tại trụ sở chính hoặc chi nhánh, văn phòng đại diện của công ty. Giám đốc
    hoặc Tổng giám đốc và người quản lý khác có nghĩa vụ cung cấp đầy đủ, kịp
    thời các thông tin về thực hiện quyền chủ sở hữu, về quản lý, điều hành và
    hoạt động kinh doanh của công ty theo yêu cầu của Kiểm soát viên.
</p>
<p>
    <strong>
        Điều 17. Nghĩa vụ của Giám đốc hoặc Tổng giám đốc và Kiểm soát viên
    </strong>
</p>
<p>
    1. Giám đốc, Tổng Giám đốc và Kiểm soát viên có các nghĩa vụ sau đây:
</p>
<p>
    a) Tuân thủ pháp luật, Điều lệ công ty, quyết định của chủ sở hữu công ty
    trong việc thực hiện các quyền và nhiệm vụ được giao;
</p>
<p>
    b) Thực hiện các quyền và nhiệm vụ được giao một cách trung thực, cẩn
    trọng, tốt nhất nhằm bảo đảm lợi ích hợp pháp tối đa của công ty và chủ sở
    hữu công ty;
</p>
<p>
    c) Trung thành với lợi ích của công ty và chủ sở hữu công ty. Không sử dụng
    thông tin, bí quyết, cơ hội kinh doanh của công ty, lạm dụng địa vị, chức
    vụ và tài sản của công ty để tư lợi hoặc phục vụ lợi ích của tổ chức, cá
    nhân khác;
</p>
<p>
    d) Thông báo kịp thời, đầy đủ và chính xác cho công ty về các doanh nghiệp
    mà họ và người có liên quan của họ làm chủ hoặc có cổ phần, phần vốn góp
    chi phối. Thông báo này được niêm yết tại trụ sở chính và chi nhánh của
    công ty;
</p>
<p>
    đ) Các nghĩa vụ khác theo quy định của Luật Doanh nghiệp.
</p>
<p>
    2. Giám đốc hoặc Tổng giám đốc không được tăng lương, trả thưởng khi công
    ty không có khả năng thanh toán đủ các khoản nợ đến hạn.<strong> </strong>
</p>
<p>
    <strong>
        Điều 18. Thù lao, tiền lương và lợi ích khác của người quản lý công ty
    </strong>
</p>
<p>
    1. Người quản lý công ty được hưởng tiền lương, thù lao, thưởng và lợi ích
    khác theo kết quả và hiệu quả kinh doanh của công ty.
</p>
<p>
    2. Chủ sở hữu công ty quyết định mức thù lao, lương và lợi ích khác. Thù
    lao, tiền lương và lợi ích khác của người quản lý công ty được tính vào chi
    phí kinh doanh theo quy định của pháp luật thuế thu nhập doanh nghiệp, pháp
    luật có liên quan và được thể hiện thành mục riêng trong báo cáo tài chính
    hằng năm của công ty.
</p>
<p>
    <strong>Điều 19. Nguyên tắc giải quyết tranh chấp nội bộ</strong>
</p>
<p>
    1. Tranh chấp nội bộ, Công ty có thể giải quyết trên phương thức tự thoả
    thuận nội bộ, nếu không được sẽ thông qua cơ quan pháp luật có thẩm quyền.
</p>
<p>
    2. Tranh chấp bên ngoài, Công ty có quyền bình đẳng trước pháp luật với mọi
    pháp nhân, thể nhân khi có tố tụng tranh chấp. Đại diện hợp pháp của Công
    ty sẽ đại diện cho Công ty trước pháp luật.
</p>
