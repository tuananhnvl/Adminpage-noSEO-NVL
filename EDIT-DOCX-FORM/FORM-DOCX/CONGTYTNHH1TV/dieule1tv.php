<p>
    <strong>Điều 26. Hiệu lực của Điều lệ</strong>
</p>
<p>
    Điều lệ này có hiệu lực kể từ ngày được cơ quan đăng ký doanh nghiệp cấp
    giấy chứng nhận đăng ký kinh doanh.
</p>
<p>
    <strong>Điều 27. Điều khoản cuối cùng</strong>
</p>
<p>
    1. Những vấn đề liên quan đến hoạt động của Công ty không được nêu trong
    Bản Điều lệ này sẽ do Luật Doanh nghiệp và các văn bản pháp luật liên quan
    khác điều chỉnh.
</p>
<p>
    2. Trong trường hợp điều lệ này có điều khoản trái pháp luật hoặc dẫn đến
    việc thi hành trái pháp luật, thì điều khoản đó không được thi hành và sẽ
    được Chủ sở hữu công ty xem xét sửa đổi.
</p>
<p>
    3. Khi muốn sửa đổi, bổ sung nội dung của Điều lệ này, chủ sở hữu công ty
    sẽ quyết định.
</p>
<p>
    Bản điều lệ này đã được chủ sở hữu công ty xem xét từng chương, từng điều
    và ký tên.
</p>
<p>
    Bản điều lệ này gồm 7chương 27 điều, được lập thành 02 bản có giá trị như
    nhau: 01 bản đăng ký tại cơ quan đăng ký kinh doanh, 01 bản lưu trữ tại trụ
    sở công ty
</p>
<p>
    Mọi sự sao chép, trích lục phải được ký xác nhận của chủ sở hữu công ty.
</p>
