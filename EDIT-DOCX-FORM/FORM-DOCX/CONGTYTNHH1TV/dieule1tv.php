<p>
    <strong>Điều 9. Tăng, giảm vốn điều lệ</strong>
</p>
<p>
    1. Công ty giảm vốn điều lệ trong các trường hợp sau đây:
</p>
<p>
    a) Hoàn trả một phần vốn góp cho chủ sở hữu công ty nếu công ty đã hoạt
    động kinh doanh liên tục từ 02 năm trở lên kể từ ngày đăng ký thành lập
    doanh nghiệp và bảo đảm thanh toán đủ các khoản nợ và nghĩa vụ tài sản khác
    sau khi đã hoàn trả phần vốn góp cho chủ sở hữu công ty;
</p>
<p>
    b) Vốn điều lệ không được chủ sở hữu công ty thanh toán đầy đủ và đúng hạn
    theo quy định tại Điều 75 của Luật Doanh Nghiệp 2020.
</p>
<p>
    2. Công ty trách nhiệm hữu hạn một thành viên tăng vốn điều lệ thông qua
    việc chủ sở hữu công ty góp thêm vốn hoặc huy động thêm vốn góp của người
    khác. Chủ sở hữu công ty quyết định hình thức tăng và mức tăng vốn điều lệ.
</p>
<div>
    <p>
        Trường hợp tăng vốn điều lệ bằng việc huy động thêm phần vốn góp của
        người khác, công ty phải tổ chức quản lý theo loại hình công ty trách
        nhiệm hữu hạn hai thành viên trở lên hoặc công ty cổ phần. Việc tổ chức
        quản lý công ty được thực hiện như sau:
    </p>
    <p>
        a) Trường hợp tổ chức quản lý theo loại hình công ty trách nhiệm hữu
        hạn hai thành viên trở lên thì công ty phải thông báo thay đổi nội dung
        đăng ký doanh nghiệp trong thời hạn 10 ngày kể từ ngày hoàn thành việc
        thay đổi vốn điều lệ;
    </p>
    <p>
        b) Trường hợp chuyển đổi thành công ty cổ phần thì công ty thực hiện
        theo quy định tại Điều 202 của Luật Doanh Nghiệp 2020.
    </p>
    <p>
        <strong>Điều 10: Phát hành trái phiếu</strong>
    </p>
    <p>
        1. Công ty trách nhiệm hữu hạn một thành viên được phát hành trái phiếu
        theo quy định của pháp luật này và quy định khác của pháp luật có liên
        quan.
    </p>
    <p>
        2. Công ty được quyền phát hành trái phiếu nếu đáp ứng các điều kiện
        sau đây:
    </p>
</div>
<p>
    a) Công ty đã thanh toán đủ cả gốc và lãi của trái phiếu đã chào bán và đã
    đến hạn thanh toán hoặc thanh toán đủ các khoản nợ đến hạn trong 03 năm
    liên tiếp trước đợt chào bán trái phiếu (nếu có), trừ trường hợp chào bán
    trái phiếu cho các chủ nợ là tổ chức tài chính được lựa chọn;
</p>
<p>
    b) Có báo cáo tài chính của năm trước liền kề năm phát hành được kiểm toán;
</p>
<p>
    c) Bảo đảm điều kiện về tỷ lệ an toàn tài chính, tỷ lệ bảo đảm an toàn
    trong hoạt động theo quy định pháp luật;
</p>
<p>
    d) Điều kiện khác theo quy định của pháp luật có liên quan.
</p>
<div>
    <p>
        3. Chủ sở hữu quyết định phát hành trái phiếu
    </p>
</div>
<p>
    <strong>Điều 11. Quyền của chủ sở hữu công ty.</strong>
</p>
<p>
    1. Quyết định nội dung Điều lệ công ty, sửa đổi, bổ sung Điều lệ công ty;
</p>
<p>
    2. Quyết định đầu tư, kinh doanh và quản trị nội bộ doanh nghiệp,
</p>
<p>
    3. Chuyển nhượng một phần hoặc toàn bộ vốn điều lệ của công ty cho tổ chức,
    cá nhân khác;
</p>
<p>
    4. Quyết định việc sử dụng lợi nhuận sau khi đã hoàn thành nghĩa vụ thuế và
    các nghĩa vụ tài chính khác của công ty;
</p>
<p>
    5. Quyết định tổ chức lại, giải thể và yêu cầu phá sản công ty;
</p>
<p>
    6. Thu hồi toàn bộ giá trị tài sản của công ty sau khi công ty hoàn thành
    giải thể hoặc phá sản;
</p>
<p>
    7. Các quyền khác theo quy định của Luật Doanh nghiệp.
</p>
<p>
    <strong>
        Điều 12. Nghĩa vụ và hạn chế đối với quyền của Chủ sở hữu Công ty
    </strong>
</p>
<p>
    1. Nghĩa vụ của Chủ sở hữu
</p>
<p>
    a) Góp vốn đầy đủ và đúng hạn như đã cam kết; trường hợp không góp đủ và
    đúng hạn số vốn đã cam kết thì phải chịu trách nhiệm về các khoản nợ và
    nghĩa vụ tài sản khác của công ty.
</p>
<p>
    b) Tuân thủ Điều lệ công ty.
</p>
<p>
    c) Phải xác định và tách biệt tài sản của chủ sở hữu công ty và tài sản của
    công ty.
</p>
<p>
    Chủ sở hữu công ty phải tách biệt các chi tiêu của cá nhân và gia đình mình
    với các chi tiêu trên cương vị là Chủ tịch công ty và Giám đốc hoặc Tổng
    giám đốc.
</p>
<p>
    d) Tuân thủ quy định của pháp luật về hợp đồng và pháp luật có liên quan
    trong việc mua, bán, vay, cho vay, thuê, cho thuê và các giao dịch khác
    giữa công ty và chủ sở hữu công ty.
</p>
<p>
    đ) Thực hiện các nghĩa vụ khác theo quy định của Luật Doanh nghiệp.
</p>
<p>
    2. Hạn chế đối vơí quyền của Chủ sở hữu công ty
</p>
<p>
    a) Chủ sở hữu công ty chỉ được quyền rút vốn bằng cách chuyển nhượng một
    phần hoặc toàn bộ số vốn điều lệ cho tổ chức hoặc cá nhân khác; trường hợp
    rút một phần hoặc toàn bộ vốn đã góp ra khỏi công ty dưới hình thức khác
    thì phải liên đới chịu trách nhiệm về các khoản nợ và nghĩa vụ tài sản khác
    của công ty.
</p>
<p>
    Trường hợp chuyển nhượng một phần vốn điều lệ cho tổ chức hoặc cá nhân
    khác, công ty phải đăng ký chuyển đổi thành công ty trách nhiệm hữu hạn hai
    thành viên trở lên trong thời hạn mười lăm ngày, kể từ ngày chuyển nhượng.
</p>
<p>
    b) Chủ sở hữu công ty không được rút lợi nhuận khi công ty không thanh toán
    đủ các khoản nợ và các nghĩa vụ tài sản khác đến hạn.
</p>
