<p>
    Bao gồm:
</p>
<p>
Tiền Việt Nam: <strong>28.000.000.000</strong> <strong>đồng</strong> (    <strong>Hai mươi tám tỷ đồng)</strong>
</p>
<p>
    - Ngoại tệ tự do chuyển đổi:
