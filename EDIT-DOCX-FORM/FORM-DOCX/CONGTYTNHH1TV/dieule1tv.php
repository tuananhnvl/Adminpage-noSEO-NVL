</p>
<p>
    2. Năm tài chính đầu tiên của Công ty sẽ bắt đầu từ ngày được cơ quan đăng
    ký kinh doanh cấp giấy chứng nhận đăng ký kinh doanh đến ngày 31/12 của năm
    đó.
</p>
<p>
    <strong>
        Điều 21. Phân phối lợi nhuận, lập quỹ, Nguyên tắc xử lý lỗ trong kinh
        doanh
    </strong>
</p>
<p>
    1. Sau khi đã hoàn thành nghĩa vụ nộp thuế và các nghĩa vụ tài chính khác
    theo quy định của pháp luật, đã thanh toán đủ (hoặc đã dành phần thanh toán
    đủ) các khoản nợ và nghĩa vụ tài sản khác đã đến hạn phải trả công ty lập
    các loại quỹ theo quy định của pháp luật
</p>
<p>
    2. Các vấn đề khác liên quan đến phân phối lợi nhuận được thực hiện theo
    quy định của pháp luật.
</p>
<p>
    3. Trường hợp quyết toán năm tài chính bị lỗ, Chủ tịch công ty được quyết
    định theo các hướng sau:
</p>
<p>
    a) Trích quỹ dự trữ để bù;
</p>
<p>
    b) Chuyển sang năm sau để trừ vào lợi nhuận của năm tài chính sau trước khi
    phân phối lợi nhuận.
</p>
<p>
    <strong>Điều 22. Sổ sách kế toán – báo cáo tài chính</strong>
</p>
<p>
    1. Sổ sách kế toán của Công ty đều mở đầy đủ và giữ đúng các quy định pháp
    luật hiện hành.
</p>
<p>
    2. Cuối mỗi năm tài chính, Công ty sẽ lập bản báo cáo tài chính để trình
    cho chủ sở hữu xem xét.
</p>
<p>
    3. Trong thời hạn 90 ngày, kể từ ngày kết thúc năm tài chính, báo cáo tài
    chính hàng năm của Công ty sẽ được gởi đến cơ quan thuế, cơ quan đăng ký
    kinh doanh và cơ quan thống kê có thẩm quyền
</p>
