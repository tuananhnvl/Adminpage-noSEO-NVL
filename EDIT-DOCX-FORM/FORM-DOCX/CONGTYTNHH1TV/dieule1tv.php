    2. Quyền và nghĩa vụ của người đại diện theo pháp luật.
</p>
<p>
    a) Người đại diện theo pháp luật của doanh nghiệp là cá nhân đại diện cho
    doanh nghiệp thực hiện các quyền và nghĩa vụ phát sinh từ giao dịch của
    doanh nghiệp, đại diện cho doanh nghiệp với tư cách người yêu cầu giải
    quyết việc dân sự, nguyên đơn, bị đơn, người có quyền lợi, nghĩa vụ liên
    quan trước Trọng tài, Tòa án và các quyền, nghĩa vụ khác theo quy định của
    pháp luật.
</p>
<div>
    <p>
        b) Thực hiện quyền và nghĩa vụ được giao một cách trung thực, cẩn
        trọng, tốt nhất nhằm bảo đảm lợi ích hợp pháp của doanh nghiệp;
    </p>
    <p>
        c) Trung thành với lợi ích của doanh nghiệp; không lạm dụng địa vị,
        chức vụ và sử dụng thông tin, bí quyết, cơ hội kinh doanh, tài sản khác
        của doanh nghiệp để tư lợi hoặc phục vụ lợi ích của tổ chức, cá nhân
        khác;
    </p>
    <p>
        d) Thông báo kịp thời, đầy đủ, chính xác cho doanh nghiệp về doanh
        nghiệp mà mình, người có liên quan của mình làm chủ hoặc có cổ phần,
        phần vốn góp theo quy định.
    </p>
    <p>
        e) Người đại diện theo pháp luật của Công ty phải thường trú tại Việt
        Nam, nếu vắng mặt quá 30 ngày ở Việt Nam thì phải uỷ quyền bằng văn bản
        cho người khác làm người đại diện theo pháp luật của Công ty
    </p>
</div>
<p>
    f) Người đại diện theo pháp luật của doanh nghiệp chịu trách nhiệm cá nhân
    đối với thiệt hại cho doanh nghiệp do vi phạm trách nhiệm quy định điều
    này.
</p>
<p>
    <strong>Điều 7: Con dấu của công ty</strong>
</p>
<div>
    <p>
        Doanh nghiệp có 02 dấu: 01 dấu bằng chữ ký số điện tử và 01 con dấu
        được khắc tại cơ sở khắc dấu.
    </p>
    <p>
        1. Dấu bằng chữ ký số được sử dụng trong các giao dịch điện tử theo quy
        định của pháp luật về giao dịch điện tử và thời hạn của chữ ký số là
        thời hạn của chứng thư số mà doanh nghiệp đăng ký với nhà cung cấp.
    </p>
    <p>
        2. Con dấu của doanh nghiệp:
    </p>
</div>
<p>
    Hình thức con dấu: Hình tròn, mực màu đỏ.
</p>
<div>
    <p>
        Nội dung con dấu:
    </p>
    <p>
        - Tên doanh nghiệp.
    </p>
    <p>
        - Mã số doanh nghiệp.
    </p>
    <p>
        (Nội dung và hình thức cụ thể của con dấu được ban hành tại quyết định
        của chủ sở hữu khi doanh nghiệp đã được cấp giấy chứng nhận đăng ký
        doanh nghiệp và có mã số doanh nghiệp.)
    </p>
    <p>
        Thời hạn sử dụng của con dấu: Đến khi doanh nghiệp chấm dứt tồn tại
        hoặc có quyết định của chủ sở hữu công ty về việc thay đổi con dấu hoặc
        quyết định hủy mẫu con dấu phù hợp với quy định của pháp luật.
    </p>
</div>
<p>
    Người đứng đầu công ty có trách nhiệm quản lý, sử dụng con dấu của công ty
    mình.
</p>
<p>
    Việc đóng dấu vào các loại văn bản, giấy tờ phải theo đúng quy định của
    pháp luật.
</p>
<p>
    Con dấu phải được để tại trụ sở công ty và phải được quản lý chặt chẽ.
    Trường hợp cần thiết để giải quyết công việc ở xa trụ sở công ty thì người
    đứng đầu công ty có thể mang con dấu đi theo và phải chịu trách nhiệm về
    việc mang con dấu ra khỏi công ty.
</p>
<p>
    Con dấu đang sử dụng bị mòn, hỏng hoặc có sự chuyển đổi hay thay đổi về nội
    dung, hình thức thì chủ sở hữu công ty có quyền ra quyết định ban hành con
    dấu mới.
</p>
<p>
    Doanh nghiệp chịu trách nhiệm về tính hợp pháp, phù hợp thuần phong mỹ tục,
    văn hóa và khả năng gây nhầm lẫn của mẫu con dấu và tranh chấp phát sinh do
    việc quản lý và sử dụng con dấu.
</p>
